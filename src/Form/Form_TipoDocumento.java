    private void llenarTabla() {
        df.setColumnCount(0);
        df.setRowCount(0);

        String[] cabezera = {"#", "Documento"};
        df.setColumnIdentifiers(cabezera);

        Object[] datos = new Object[df.getColumnCount()];

        ArrayList<TipoDocumentoDTO> lista = new ArrayList<>();
        lista = (ArrayList<TipoDocumentoDTO>) tipoDocumento.Listar();
        if (lista != null) {

            for (int i = 0; i < lista.size(); i++) {
                TipoDocumentoDTO c = lista.get(i);

                datos[0] = c.getIdTipoDocumento();
                datos[1] = c.getNombre();
                

                df.addRow(datos);
            }

            tabla.setModel(df);

        }
    }
    
    
