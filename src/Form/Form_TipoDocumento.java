    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
        String ID = JOptionPane.showInputDialog("Ingrese el ID a buscar");
        
        if (ID.matches("[0-9]*")){
            TipoDocumentoDTO tipo = tipoDocumento.Buscar(Integer.parseInt(ID));
            
            if (tipo !=  null){
                txt_nombre.setText(tipo.getNombre());
            } else {
                JOptionPane.showMessageDialog(null, "No encontrado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese solo valores enteros");
        }
        
    }//GEN-LAST:event_btn_BuscarActionPerformed

    private void btn_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AgregarActionPerformed
        if (!txt_nombre.getText().isEmpty()){     
            String mensaje = tipoDocumento.Agregar(txt_nombre.getText());
            JOptionPane.showMessageDialog(null, mensaje);
        }
        llenarTabla();
    }//GEN-LAST:event_btn_AgregarActionPerformed

    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
       
        if (!txt_nombre.getText().isEmpty()){     
            if (id != 0){
                String mensaje = tipoDocumento.Actualizar(id, txt_nombre.getText());
                JOptionPane.showMessageDialog(null, mensaje);
            } else {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna fila");
            }
            
        }
        llenarTabla();
    }//GEN-LAST:event_btn_ActualizarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        id = (int) (tabla.getValueAt(tabla.getSelectedRow(), 0));
       
      if (id != 0){
          TipoDocumentoDTO tipo = tipoDocumento.Buscar(id);
          
          if (tipo != null){
              txt_nombre.setText(tipo.getNombre());
          }
      } else {
          JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna fila");
      }
        
    }//GEN-LAST:event_tablaMouseClicked


